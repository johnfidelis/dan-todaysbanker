function FormDots({ form }: { form?: string | null }) {
  const letters = form ? form.split("").slice(-5) : [];

  const getColor = (ch: string | null) => {
    if (!ch) return "#b0b0b0";
    const c = ch.toUpperCase();
    if (c === "W") return "#68d100";
    if (c === "D") return "#ffc501";
    if (c === "L") return "#ec1c24";
    return "#b0b0b0";
  };

  const slots: (string | null)[] =
    letters.length > 0
      ? [...Array(Math.max(0, 5 - letters.length)).fill(null), ...letters]
      : Array(5).fill(null);

  return (
    <div className="content-stretch flex gap-[2.515px] items-center justify-center relative shrink-0">
      {slots.map((ch, idx) => (
        <div
          key={idx}
          className="bg-[#b0b0b0] content-stretch flex h-[11.989px] items-center justify-center px-[3.633px] py-[1.453px] rounded-[36.842px] shrink-0 w-[11.988px]"
          style={{ backgroundColor: getColor(ch) }}
          data-name="TeamMatchStatus"
        />
      ))}
    </div>
  );
}


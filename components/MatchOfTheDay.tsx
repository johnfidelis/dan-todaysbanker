function ListFTmatches({ match }: { match: NormalizedMatch }) {
  const { homeTeam, awayTeam, odds, date, homeForm, awayForm } = match;

  const odd1 = odds.odds1 != null ? odds.odds1.toFixed(2) : "-";
  const oddX = odds.oddsX != null ? odds.oddsX.toFixed(2) : "-";
  const odd2 = odds.odds2 != null ? odds.odds2.toFixed(2) : "-";

  const time = new Date(date).toLocaleTimeString("en-GB", {
    hour: "2-digit",
    minute: "2-digit",
  });

  return (
    <div
      className="content-stretch flex flex-col gap-[10px] items-start overflow-clip relative shrink-0 w-full"
      data-name="listFTmatches"
    >
      <Frame4 />
      <div
        className="bg-white content-stretch flex flex-col items-start py-[10px] relative rounded-[18px] shrink-0 w-full"
        data-name="listItemNew"
      >
        <Frame22
          homeName={homeTeam.name}
          awayName={awayTeam.name}
          time={time}
          odd1={odd1}
          oddX={oddX}
          odd2={odd2}
          homeLogo={homeTeam.logo}
          awayLogo={awayTeam.logo}
          homeForm={homeForm}
          awayForm={awayForm}
        />
      </div>
    </div>
  );
}


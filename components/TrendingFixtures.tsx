/* --- root client component --- */

export default function TrendingFixtures({ fixtures }: Props) {
  const [activeIndex, setActiveIndex] = useState(0);

  if (!fixtures || fixtures.length === 0) return null;

  const safeIndex = ((activeIndex % fixtures.length) + fixtures.length) % fixtures.length;
  const activeMatch = fixtures[safeIndex];

  const handlePrev = () => {
    setActiveIndex((prev) => (prev - 1 + fixtures.length) % fixtures.length);
  };

  const handleNext = () => {
    setActiveIndex((prev) => (prev + 1) % fixtures.length);
  };

  return (
    <div className="content-stretch flex flex-col gap-[10px] items-start relative size-full">
      <Frame1 />
      <DateSelector
        total={fixtures.length}
        activeIndex={safeIndex}
        onPrev={handlePrev}
        onNext={handleNext}
      />
      <ListItem match={activeMatch} />
    </div>
  );
}
